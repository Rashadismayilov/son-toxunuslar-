const basketBtn = document.querySelectorAll("#bagicon")

function createStorage() {
  if (!localStorage.getItem("basket")) {
    localStorage.setItem("basket", JSON.stringify([]))
  }
}

basketBtn.forEach(item => {
  item.addEventListener("click", function () {
    const id = this.parentElement.parentElement.getAttribute("data-id")
    const name = this.parentElement.nextElementSibling.children[7].innerText
    const price = this.parentElement.nextElementSibling.children[9].innerText
    const image = this.parentElement.children[5].src
    createStorage();
    let basket = createBasket(id, name, price, image)
    localStorage.setItem("basket", JSON.stringify(basket))
  })
})

function createBasket(id, name, price, image) {
  const basket = JSON.parse(localStorage.getItem("basket"));

  const existMeal = basket.find(meal => meal.id == id)

  if (!existMeal) {
    basket.push({
      count: 1,
      id,
      name,
      price,
      image,
    })
  } else {
    existMeal.count++
  }

  return basket;
}